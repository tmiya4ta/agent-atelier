// bookmark エントリ生成
function bm(a) {
  const url = murl(a.name, a.emulate);
  return { key: `mock::${url}`, protoId: "mock", url, name: a.name,
    auth: "", authRef: undefined, persona: "", channel: "", emulate: a.emulate, mockReply: a.mockReply };
}
function win(a, pos) {
  const url = murl(a.name, a.emulate);
  const config = { url, name: a.name, auth: "", persona: "", channel: "", emulate: a.emulate, mockReply: a.mockReply };
  return { protoId: "mock", config, pos, activeTab: "chat", pinned: false };
}

